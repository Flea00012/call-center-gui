package sample;


/**
 * class specifies the properties that an EmployeeStatus should have.
 * <p>
 * The {@code EmployeeStatus} class is an Enum type to specify constant properties
 * that define the EmployeeStatus. These include AVAILABLE and UNAVAILABLE, as well as
 * ON_LUNCH and GONE_FOR_THE_DAY.
 *
 * @author leefowler
 */
public enum EmployeeStatus {
    AVAILABLE,
    UNAVAILABLE,
    ON_LUNCH,
    GONE_FOR_THE_DAY,
    IN_A_MEETING
}
